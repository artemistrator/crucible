const fs = require('fs');
const path = require('path');

// Minimal 1x1 transparent PNG
const png = Buffer.from([
  0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, // PNG signature
  0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52, // IHDR chunk start
  0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01, // 1x1 pixels
  0x08, 0x06, 0x00, 0x00, 0x00, 0x1F, 0x15, 0xC4, // 8-bit/color/alpha
  0x89, 0x00, 0x00, 0x00, 0x0A, 0x49, 0x44, 0x41, // IDAT chunk start
  0x54, 0x08, 0x1D, 0x01, 0x02, 0x00, 0xFF, 0xFF, // Compressed data
  0x00, 0x00, 0x00, 0x02, 0x00, 0x01, 0x00, 0x00, // IDAT checksum
  0x00, 0x00, 0x00, 0x49, 0x45, 0x4E, 0x44, 0xAE, // IEND chunk
  0x42, 0x60, 0x82                                 // IEND checksum
]);

const publicDir = path.join(__dirname, '..', 'public');

if (!fs.existsSync(publicDir)) {
  fs.mkdirSync(publicDir);
}

// Write placeholders
fs.writeFileSync(path.join(publicDir, 'pwa-192x192.png'), png);
fs.writeFileSync(path.join(publicDir, 'pwa-512x512.png'), png);
console.log('PWA placeholder icons generated.');
